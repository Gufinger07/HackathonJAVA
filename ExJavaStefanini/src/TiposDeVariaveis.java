
public class TiposDeVariaveis {

	public static void main(String[] args) {
		
		double numeroGrande;
		numeroGrande = 2147483649f;
		
		byte numeroMenor1;
		numeroMenor1 = 120;
		
		short numeroMenor2;
		numeroMenor2 = 150;
		
		String frase;
		frase = "Bem vindo Devs!";
		
		String fraseComNumeros;
		fraseComNumeros = "0x2202";
		
		double numeroGrande2;
		numeroGrande2 = 99999.987;
		
		System.out.println(numeroGrande);
		System.out.println(numeroMenor1);
		System.out.println(numeroMenor2);
		System.out.println(frase);
		System.out.println(fraseComNumeros);
		System.out.println(numeroGrande2);	
		 
	}

}
